function browseSubmodule(fig)

    mainModelPath = getappdata(fig, 'mainModelPath');

    if isempty(mainModelPath) || ...
            exist(mainModelPath, 'file') ~= 2

        errordlg( ...
            'Select a valid Main Model first.', ...
            'Model Setup');

        return;

    end

    ut_setStatus( ...
        fig, ...
        'Loading model hierarchy...', ...
        'working');

    drawnow;

    try

        [~, modelName] = fileparts(mainModelPath);

        load_system(mainModelPath);

        subsystemPaths = find_system( ...
            modelName, ...
            'SearchDepth', 2, ...
            'BlockType', 'SubSystem');

        subsystemPaths = subsystemPaths( ...
            ~strcmp(subsystemPaths, modelName));

        if isempty(subsystemPaths)

            ut_setStatus( ...
                fig, ...
                'No submodules found.', ...
                'error');

            errordlg( ...
                'No submodules were found in the selected model.', ...
                'Model Setup');

            return;

        end

        [selectedIndex, selectionMade] = listdlg( ...
            'PromptString', 'Select Submodule', ...
            'SelectionMode', 'single', ...
            'ListString', subsystemPaths, ...
            'ListSize', [500 350]);

        if ~selectionMade

            ut_setStatus( ...
                fig, ...
                'Submodule selection cancelled.', ...
                'ready');

            return;

        end

        selectedSubsystem = subsystemPaths{selectedIndex};

        ut_setState( ...
            fig, ...
            'submodulePath', ...
            selectedSubsystem);

        setApplicationControl( ...
            fig, ...
            'submoduleBox', ...
            'String', ...
            selectedSubsystem);

        ut_setStatus( ...
            fig, ...
            'Submodule selected.', ...
            'success');

    catch ME

        ut_setStatus( ...
            fig, ...
            'Unable to load submodules.', ...
            'error');

        errordlg( ...
            ME.message, ...
            'Submodule Error');

    end

end


