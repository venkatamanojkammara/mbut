function batchPaths = findBatchXmlFiles(rootFolder)

    batchPaths = {};

    folderPaths = strsplit( ...
        genpath(rootFolder), ...
        pathsep);

    folderPaths = folderPaths( ...
        ~cellfun('isempty', folderPaths));

    for folderIndex = 1:numel(folderPaths)

        currentFolder = folderPaths{folderIndex};

        [~, folderName] = fileparts(currentFolder);

        if ~strncmp(folderName, 'Test_', 5)
            continue;
        end

        xmlFiles = dir(fullfile( ...
            currentFolder, ...
            '*_TestCase.xml'));

        for fileIndex = 1:numel(xmlFiles)

            batchPaths{end + 1, 1} = fullfile( ... %#ok<AGROW>
                currentFolder, ...
                xmlFiles(fileIndex).name);

        end

    end

end


