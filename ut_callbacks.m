function addSelectedCases(fig)

    availableList = getApplicationControl( ...
        fig, ...
        'testSeqList');

    selectedList = getApplicationControl( ...
        fig, ...
        'selectedTestSeqList');

    availableItems = normalizeListboxStrings( ...
        get(availableList, 'String'));

    selectedIndices = get( ...
        availableList, ...
        'Value');

    if isempty(availableItems) || ...
            isempty(selectedIndices)

        return;

    end

    selectedItems = normalizeListboxStrings( ...
        get(selectedList, 'String'));

    for index = 1:numel(selectedIndices)

        itemIndex = selectedIndices(index);

        if itemIndex > numel(availableItems)
            continue;
        end

        itemName = availableItems{itemIndex};

        if ~any(strcmp(selectedItems, itemName))
            selectedItems{end + 1, 1} = itemName; %#ok<AGROW>
        end

    end

    set( ...
        selectedList, ...
        'String', selectedItems, ...
        'Value', []);

    ut_setStatus( ...
        fig, ...
        'Test case selection updated.', ...
        'success');

end


