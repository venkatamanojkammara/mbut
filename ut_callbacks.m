function updateBatchCount(fig)

    appData = guidata(fig);

    if isempty(appData) || ...
            ~isfield(appData, 'BatchRunView')

        return;

    end

    if ~isfield( ...
            appData.BatchRunView, ...
            'FileCountText')

        return;

    end

    fileCountText = ...
        appData.BatchRunView.FileCountText;

    if isempty(fileCountText) || ...
            ~ishandle(fileCountText)

        return;

    end

    batchPaths = getappdata( ...
        fig, ...
        'excelPathList');

    fileCount = numel(batchPaths);

    if fileCount == 1
        countLabel = '1 file';
    else
        countLabel = sprintf( ...
            '%d files', ...
            fileCount);
    end

    set( ...
        fileCountText, ...
        'String', ...
        countLabel);

end