function parseTestCases(fig, xmlPath)

    xmlDocument = xmlread(xmlPath);

    testCaseNodes = xmlDocument.getElementsByTagName( ...
        'test-case');

    testCaseNames = {};
    testCaseIDs = {};
    testCaseMap = containers.Map;

    for nodeIndex = 0:testCaseNodes.getLength - 1

        testCaseNode = testCaseNodes.item(nodeIndex);

        testCaseID = strtrim(char( ...
            testCaseNode.getAttribute( ...
                'test-case-id')));

        testCaseName = strtrim(char( ...
            testCaseNode.getAttribute( ...
                'test-case-name')));

        if isempty(testCaseID) || ...
                isempty(testCaseName)

            continue;

        end

        testCaseNames{end + 1, 1} = testCaseName; %#ok<AGROW>
        testCaseIDs{end + 1, 1} = testCaseID; %#ok<AGROW>

        testCaseMap(testCaseName) = testCaseID;

    end

    if isempty(testCaseNames)

        error( ...
            'ut_callbacks:NoTestCases', ...
            'No valid test cases were found in the XML file.');

    end

    availableList = getApplicationControl( ...
        fig, ...
        'testSeqList');

    selectedList = getApplicationControl( ...
        fig, ...
        'selectedTestSeqList');

    set( ...
        availableList, ...
        'String', testCaseNames, ...
        'Value', [], ...
        'Min', 0, ...
        'Max', max(2, numel(testCaseNames)));

    set( ...
        selectedList, ...
        'String', {}, ...
        'Value', []);

    ut_setState( ...
        fig, ...
        'testCaseNames', ...
        testCaseNames);

    ut_setState( ...
        fig, ...
        'testCaseIDs', ...
        testCaseIDs);

    ut_setState( ...
        fig, ...
        'testCaseMap', ...
        testCaseMap);

end


function loadTestCases(fig)

    xmlPath = getControlString( ...
        fig, ...
        'mtcdXlsBox');

    sampleTime = str2double(getControlString( ...
        fig, ...
        'stepTimeBox'));

    if isempty(xmlPath) || ...
            exist(xmlPath, 'file') ~= 2

        errordlg( ...
            'Select a valid XML test case file.', ...
            'Test Case Management');

        return;

    end

    if isnan(sampleTime) || ...
            ~isfinite(sampleTime) || ...
            sampleTime <= 0

        errordlg( ...
            'Sample Time must be a positive numeric value.', ...
            'Test Case Management');

        return;

    end

    savePath = getappdata( ...
        fig, ...
        'savePath');

    submodulePath = getappdata( ...
        fig, ...
        'submodulePath');

    if isempty(savePath) || ...
            isempty(submodulePath)

        errordlg( ...
            'Complete Model Setup first.', ...
            'Test Case Management');

        return;

    end

    [~, submoduleName] = fileparts(submodulePath);

    testFolderPath = fullfile( ...
        savePath, ...
        ['Test_' submoduleName]);

    if exist(testFolderPath, 'dir') ~= 7
        mkdir(testFolderPath);
    end

    [~, xmlName, xmlExtension] = fileparts(xmlPath);

    targetXmlPath = fullfile( ...
        testFolderPath, ...
        [xmlName xmlExtension]);

    sourceFolder = fileparts(xmlPath);

    if ~strcmpi(sourceFolder, testFolderPath)

        copyfile( ...
            xmlPath, ...
            targetXmlPath);

        xmlPath = targetXmlPath;

    end

    dataFolders = dir(fullfile( ...
        fileparts(xmlPath), ...
        '*_data'));

    dataFolders = dataFolders([dataFolders.isdir]);

    if isempty(dataFolders)

        errordlg( ...
            'No *_data folder was found next to the XML file.', ...
            'Load Test Cases');

        return;

    end

    dataDirectory = fullfile( ...
        fileparts(xmlPath), ...
        dataFolders(1).name);

    ut_setState( ...
        fig, ...
        'mtcdXlsPath', ...
        xmlPath);

    setApplicationControl( ...
        fig, ...
        'mtcdXlsBox', ...
        'String', ...
        xmlPath);

    ut_setStatus( ...
        fig, ...
        'Loading test cases...', ...
        'working');

    drawnow;

    try

        build_from_xml( ...
            xmlPath, ...
            sampleTime, ...
            dataDirectory);

        matDirectory = fullfile( ...
            testFolderPath, ...
            'mat_file');

        if exist(matDirectory, 'dir') ~= 7
            mkdir(matDirectory);
        end

        matFiles = dir(fullfile( ...
            testFolderPath, ...
            '*.mat'));

        for fileIndex = 1:numel(matFiles)

            sourceMatPath = fullfile( ...
                testFolderPath, ...
                matFiles(fileIndex).name);

            targetMatPath = fullfile( ...
                matDirectory, ...
                matFiles(fileIndex).name);

            if exist(targetMatPath, 'file') == 2
                delete(targetMatPath);
            end

            movefile( ...
                sourceMatPath, ...
                targetMatPath);

        end

        parseTestCases( ...
            fig, ...
            xmlPath);

        ut_setStatus( ...
            fig, ...
            'Test cases loaded successfully.', ...
            'success');

        msgbox( ...
            'Test cases loaded successfully.', ...
            'Load Test Cases');

    catch ME

        ut_setStatus( ...
            fig, ...
            'Unable to load test cases.', ...
            'error');

        errordlg( ...
            ME.message, ...
            'Load Test Cases');

    end

end


