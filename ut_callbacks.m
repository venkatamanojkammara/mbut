function executeSimulation(fig)

    xmlPath = getappdata( ...
        fig, ...
        'mtcdXlsPath');

    selectedList = getApplicationControl( ...
        fig, ...
        'selectedTestSeqList');

    selectedTestCases = normalizeListboxStrings( ...
        get(selectedList, 'String'));

    testCaseMap = getappdata( ...
        fig, ...
        'testCaseMap');

    if isempty(xmlPath) || ...
            isempty(selectedTestCases)

        errordlg( ...
            ['Select a test case file and at least one ' ...
             'test case.'], ...
            'Execute Simulation');

        return;

    end

    if isempty(testCaseMap) || ...
            ~isa(testCaseMap, 'containers.Map')

        errordlg( ...
            ['Test case mapping is unavailable. ' ...
             'Reload the XML file.'], ...
            'Execute Simulation');

        return;

    end

    testCaseIDs = cell( ...
        numel(selectedTestCases), ...
        1);

    for testCaseIndex = 1:numel(selectedTestCases)

        testCaseName = selectedTestCases{testCaseIndex};

        if ~isKey(testCaseMap, testCaseName)

            errordlg( ...
                ['Unable to resolve Test Case ID for: ' ...
                 testCaseName], ...
                'Execute Simulation');

            return;

        end

        testCaseIDs{testCaseIndex} = ...
            testCaseMap(testCaseName);

    end

    milEnabled = logical(get( ...
        getApplicationControl(fig, 'milChk'), ...
        'Value'));

    silEnabled = logical(get( ...
        getApplicationControl(fig, 'silChk'), ...
        'Value'));

    if ~milEnabled && ~silEnabled

        errordlg( ...
            'Select MIL, SIL, or both.', ...
            'Execute Simulation');

        return;

    end

    coverageControl = getApplicationControl( ...
        fig, ...
        'codeCoverageType');

    coverageValue = get( ...
        coverageControl, ...
        'Value');

    ut_setState( ...
        fig, ...
        'code_coverage_type', ...
        coverageValue);

    ut_setState( ...
        fig, ...
        'TestseqID', ...
        testCaseIDs);

    [frameName, framePath] = resolveTestFrame( ...
        fig, ...
        xmlPath);

    if isempty(frameName)

        errordlg( ...
            'No test frame SLX file was found.', ...
            'Execute Simulation');

        return;

    end

    ut_setStatus( ...
        fig, ...
        'Executing simulation...', ...
        'working');

    drawnow;

    try

        if ~bdIsLoaded(frameName)
            load_system(framePath);
        end

        fcn_Simulate_frame( ...
            xmlPath, ...
            testCaseIDs, ...
            milEnabled, ...
            silEnabled, ...
            frameName);

        ut_setStatus( ...
            fig, ...
            'Simulation completed.', ...
            'success');

    catch ME

        ut_setStatus( ...
            fig, ...
            'Simulation failed.', ...
            'error');

        errordlg( ...
            ME.message, ...
            'Execute Simulation');

    end

end


