    userChoice = questdlg( ...
        sprintf( ...
            ['Selected folder:\n\n%s\n\n' ...
             'Choose the batch operation.'], ...
            rootFolder), ...
        'Confirm Batch Run', ...
        'Continue with Batch Run', ...
        'Generate only Reports', ...
        'Cancel', ...
        'Continue with Batch Run');

    if isempty(userChoice) || ...
            strcmp(userChoice, 'Cancel')

        ut_setStatus( ...
            fig, ...
            'Batch run cancelled.', ...
            'ready');

        return;

    end

    ut_setStatus( ...
        fig, ...
        'Scanning batch folders...', ...
        'working');

    drawnow;

    batchPaths = findBatchXmlFiles(rootFolder);

    if isempty(batchPaths)

        ut_setStatus( ...
            fig, ...
            'No batch XML files found.', ...
            'error');

        errordlg( ...
            ['No Test_* folders containing *_TestCase.xml ' ...
             'files were found.'], ...
            'Batch Run');

        return;

    end

    updateBatchPathList( ...
        fig, ...
        batchPaths);

    if strcmp(userChoice, 'Generate only Reports')

        generateReportForPaths( ...
            fig, ...
            batchPaths);

    else

        executeBatch(fig);

    end

end


