function value = getPopupValue(controlHandle)

    popupItems = get( ...
        controlHandle, ...
        'String');

    selectedIndex = get( ...
        controlHandle, ...
        'Value');

    if ischar(popupItems)

        value = popupItems;

        return;

    end

    if isempty(popupItems)

        value = '';

        return;

    end

    selectedIndex = max( ...
        1, ...
        min(selectedIndex, numel(popupItems)));

    value = popupItems{selectedIndex};

end


function isPlaceholder = isPlaceholderValue(value)

    isPlaceholder = isempty(value) || ...
        (~isempty(value) && value(1) == '<');

end


