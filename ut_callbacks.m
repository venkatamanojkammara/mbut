function refreshSignals(fig)

    signalDropdown = getApplicationControl( ...
        fig, ...
        'sim_ddSignals');

    xmlPath = getappdata( ...
        fig, ...
        'mtcdXlsPath');

    if isempty(xmlPath) || ...
            exist(xmlPath, 'file') ~= 2

        set( ...
            signalDropdown, ...
            'String', {'<No signals>'}, ...
            'Value', 1);

        return;

    end

    dataFolders = dir(fullfile( ...
        fileparts(xmlPath), ...
        '*_data'));

    dataFolders = dataFolders([dataFolders.isdir]);

    if isempty(dataFolders)

        set( ...
            signalDropdown, ...
            'String', {'<No Data Folder>'}, ...
            'Value', 1);

        return;

    end

    dataFolderPath = fullfile( ...
        fileparts(xmlPath), ...
        dataFolders(1).name);

    outputPortPath = fullfile( ...
        dataFolderPath, ...
        'outports.json');

    if exist(outputPortPath, 'file') ~= 2

        set( ...
            signalDropdown, ...
            'String', {'<No Outports File>'}, ...
            'Value', 1);

        return;

    end

    try

        outputPortData = readJsonFile(outputPortPath);

        signalNames = extractSignalNames( ...
            outputPortData);

        if isempty(signalNames)
            signalNames = {'<No signals>'};
        end

        set( ...
            signalDropdown, ...
            'String', signalNames, ...
            'Value', 1);

    catch

        set( ...
            signalDropdown, ...
            'String', {'<Invalid Outports File>'}, ...
            'Value', 1);

    end

end


function data = readJsonFile(filePath)

    fileIdentifier = fopen( ...
        filePath, ...
        'r');

    if fileIdentifier == -1

        error( ...
            'ut_callbacks:JsonReadError', ...
            'Unable to open JSON file: %s', ...
            filePath);

    end

    cleanupObject = onCleanup( ...
        @() fclose(fileIdentifier)); %#ok<NASGU>

    rawText = fread( ...
        fileIdentifier, ...
        '*char')';

    rawText = strtrim(rawText);

    if isempty(rawText)
        data = [];
        return;
    end

    data = jsondecode(rawText);

end


function signalNames = extractSignalNames(outputPortData)

    signalNames = {};

    if isempty(outputPortData)
        return;
    end

    if isstruct(outputPortData)

        for dataIndex = 1:numel(outputPortData)

            if isfield( ...
                    outputPortData(dataIndex), ...
                    'signal')

                signalName = strtrim( ...
                    outputPortData(dataIndex).signal);

                if ~isempty(signalName)

                    signalNames{end + 1, 1} = ...
                        signalName; %#ok<AGROW>

                end

            end

        end

    end

end


