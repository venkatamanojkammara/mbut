function generateReport(fig)

    targetCoverage = str2double(getControlString( ...
        fig, ...
        'targetCoverageBox'));

    if isnan(targetCoverage) || ...
            ~isfinite(targetCoverage) || ...
            targetCoverage < 0 || ...
            targetCoverage > 100

        errordlg( ...
            'Target Coverage must be between 0 and 100.', ...
            'Generate Report');

        return;

    end

    coverageDropdown = getApplicationControl( ...
        fig, ...
        'coverageTypeDropdown');

    coverageType = getPopupValue(coverageDropdown);

    ut_setState( ...
        fig, ...
        'target_coverage', ...
        targetCoverage);

    ut_setState( ...
        fig, ...
        'coverage_type', ...
        coverageType);

    reportPaths = getappdata( ...
        fig, ...
        'excelPathList');

    if isempty(reportPaths)

        xmlPath = getappdata( ...
            fig, ...
            'mtcdXlsPath');

        if isempty(xmlPath)

            errordlg( ...
                'Load a test case file first.', ...
                'Generate Report');

            return;

        end

        reportPaths = {xmlPath};

    end

    generateReportForPaths( ...
        fig, ...
        reportPaths);

end


