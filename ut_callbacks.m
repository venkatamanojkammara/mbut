function varargout = ut_callbacks(action, fig, varargin)

    if nargin < 2 || isempty(fig) || ~ishandle(fig)
        error( ...
            'ut_callbacks:InvalidFigure', ...
            'A valid application figure handle is required.');
    end

    if nargin < 1 || isempty(action)
        error( ...
            'ut_callbacks:InvalidAction', ...
            'A callback action is required.');
    end

    switch action

        case 'browseMainModel'
            browseMainModel(fig);

        case 'browseSubmodule'
            browseSubmodule(fig);

        case 'browseSavePath'
            browseSavePath(fig);

        case 'saveModelInfo'
            saveModelInformation(fig);

        case 'generateTestFrame'
            generateTestFrame(fig);

        case 'openTestFrame'
            openTestFrame(fig);

        case 'browseTestCaseFile'
            browseTestCaseFile(fig);

        case 'loadTestCases'
            loadTestCases(fig);

        case 'availableDoubleClick'
            availableTestCaseDoubleClick(fig);

        case 'selectAllTestCases'
            selectAllTestCases(fig);

        case 'deleteSelectedTestCases'
            deleteSelectedTestCases(fig);

        case 'executeSimulation'
            executeSimulation(fig);

        case 'generateCode'
            generateCode(fig);

        case 'runTestCaseGenerator'
            runTestCaseGenerator(fig);

        case 'addBatchFile'
            addBatchFile(fig);

        case 'batchSelectionChanged'
            batchSelectionChanged(fig);

        case 'deleteBatchFile'
            deleteBatchFile(fig);

        case 'executeBatch'
            executeBatch(fig);

        case 'automaticBatchRun'
            automaticBatchRun(fig);

        case 'runBatchTestCases'
            runBatchTestCases(fig);

        case 'refreshPlotFolders'
            refreshPlotFolders(fig);

        case 'plotFolderChanged'
            refreshSignals(fig);

        case 'plotSignal'
            plotSignal(fig);

        case 'generateReport'
            generateReport(fig);

        otherwise
            error( ...
                'ut_callbacks:UnknownAction', ...
                'Unknown callback action: %s', ...
                action);

    end

    if nargout > 0
        varargout{1} = [];
    end

end


